def findGoodSeed(startingSeed, numberOfSeeds):
	s = startingSeed
	seedArray = []
	nr = 0

	while nr < numberOfSeeds:
		n = OpenSimplex(seed=s)
		nH = 0
		a = 1
		f = 1

		for o in range(0, OCTAVES):
			sX = (WIDTH / 2) / SCALE * f
			sY = (HEIGHT / 2) / SCALE * f

			v = n.noise2(sX, sY)
			nH += v * a

			a *= PERSISTANCE
			f *= LACUNARITY

		if int((nH + 1) * 128) > 245:
			seedArray.append(s)
			nr += 1
		s += 1

	return seedArray

